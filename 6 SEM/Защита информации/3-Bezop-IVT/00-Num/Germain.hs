   -- Функция, конструирующая потенциально бесконечный
   -- список чисел Софи Жермен
   -----------------------------
   germain:: [(Integer,Integer)]
   germain = [(p,2*p+1)| p <- primes, isPrime(2*p+1)]

   -- ******************************************************
   -- Функция, конструирующая простым перебором потенциально
   -- бесконечный список простых чисел 
   -- (н е э ф ф е к т и в н а я  функция)
   -------------------------------------------
   primes = 2 : [n | n <- [1,3..], isPrime1 n]
            where isPrime1 x = divisors x==[1]

   -- ******************************************
   -- Функция, конструирующая список собственных 
   -- делителей натурального числа n
   ---------------------------------
   divisors :: Integer -> [Integer]
   divisors n = [x | x <- [1..div n 2], mod n x==0]

   -- **********************************************
   -- Функция-предикат, распознающая простое число x
   -------------------------------------------------
   isPrime x = dividers x==[1,x]

      -- ************************************************
   -- Функция, конструирующая список простых чисел, не
   -- превосходящих заданного натурального числа n
   -----------------------------------------------
   dividers n = [x | x <- [1..n], mod n x==0]

